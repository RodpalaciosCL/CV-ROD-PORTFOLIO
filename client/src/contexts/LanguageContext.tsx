export const LanguageProvider: React.FC<{ children: React.ReactNode }> = ({ children }) => {
  const [language, setLanguage] = useState<Language>('en');
  const clientConfig = getClientConfig();

  useEffect(() => {
    // Cargar idioma desde localStorage, pero siempre comenzar en inglés por defecto
    const savedLanguage = localStorage.getItem('language') as Language;
    if (savedLanguage && (savedLanguage === 'es' || savedLanguage === 'en')) {
      setLanguage(savedLanguage);
    } else {
      // Si no hay idioma guardado, establecer inglés por defecto
      setLanguage('en');
      localStorage.setItem('language', 'en');
    }
  }, []);

  const changeLanguage = (lang: Language) => {
    setLanguage(lang);
    localStorage.setItem('language', lang);
  };

  const t = (key: string): string => {
    let translation = (translations[language] as any)[key] || key;
    
    // Reemplazar dinámicamente referencias al cliente
    if (typeof translation === 'string') {
      translation = translation.replace(/Metso/g, clientConfig.name);
    }
    
    return translation;
  };

  return (
    <LanguageContext.Provider value={{ 
      language, 
      setLanguage: changeLanguage, 
      t,
      clientName: clientConfig.name 
    }}>
      {children}
    </LanguageContext.Provider>
  );
};

export const useLanguage = () => {
  const context = useContext(LanguageContext);
  if (context === undefined) {
    throw new Error('useLanguage must be used within a LanguageProvider');
  }
  return context;
};
